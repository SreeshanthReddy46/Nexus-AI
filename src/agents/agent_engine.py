class SimplePDFWriter:
    def __init__(self):
        self.objects = []
        self.offsets = []

    def add_object(self, content_bytes):
        obj_id = len(self.objects) + 1
        self.objects.append((obj_id, content_bytes))
        return obj_id

    def build(self):
        pdf = bytearray()
        pdf.extend(b"%PDF-1.4\n")
        
        for obj_id, content in self.objects:
            self.offsets.append(len(pdf))
            pdf.extend(f"{obj_id} 0 obj\n".encode('ascii'))
            pdf.extend(content)
            pdf.extend(b"\nendobj\n")
            
        xref_offset = len(pdf)
        pdf.extend(b"xref\n")
        pdf.extend(f"0 {len(self.objects) + 1}\n".encode('ascii'))
        pdf.extend(b"0000000000 65535 f \n")
        for offset in self.offsets:
            pdf.extend(f"{offset:010d} 00000 n \n".encode('ascii'))
            
        pdf.extend(b"trailer\n")
        pdf.extend(f"<< /Size {len(self.objects) + 1} /Root 1 0 R >>\n".encode('ascii'))
        pdf.extend(b"startxref\n")
        pdf.extend(f"{xref_offset}\n".encode('ascii'))
        pdf.extend(b"%%EOF\n")
        return pdf

def generate_pdf_binary(title, content):
    writer = SimplePDFWriter()
    
    catalog_id = 1
    pages_id = 2
    font_bold_id = 3
    font_regular_id = 4
    page_id = 5
    content_id = 6
    
    writer.objects.append((catalog_id, b"<< /Type /Catalog /Pages 2 0 R >>"))
    writer.objects.append((pages_id, b"<< /Type /Pages /Kids [5 0 R] /Count 1 >>"))
    writer.objects.append((font_bold_id, b"<< /Type /Font /Subtype /Type1 /BaseFont /Helvetica-Bold >>"))
    writer.objects.append((font_regular_id, b"<< /Type /Font /Subtype /Type1 /BaseFont /Helvetica >>"))
    
    # Build text stream
    stream_content = bytearray()
    
    # Title
    stream_content.extend(b"BT\n")
    stream_content.extend(b"/F1 16 Tf\n")
    stream_content.extend(b"18 TL\n")
    stream_content.extend(b"50 780 Td\n")
    escaped_title = title.replace('\\', '\\\\').replace('(', '\\(').replace(')', '\\)')
    stream_content.extend(f"({escaped_title}) Tj T*\n".encode('utf-8'))
    stream_content.extend(b"ET\n")
    
    # Meta Details (Date & Nexus AI)
    stream_content.extend(b"BT\n")
    stream_content.extend(b"/F2 9 Tf\n")
    stream_content.extend(b"12 TL\n")
    stream_content.extend(b"50 755 Td\n")
    date_str = datetime.date.today().isoformat()
    stream_content.extend(f"(Date: {date_str}   |   Generated by Nexus AI Engine) Tj T*\n".encode('ascii'))
    stream_content.extend(b"ET\n")
    
    # Separator Line
    stream_content.extend(b"1.5 w\n")
    stream_content.extend(b"50 740 m 545 740 l S\n")
    
    # Body Text
    stream_content.extend(b"BT\n")
    stream_content.extend(b"/F2 10 Tf\n")
    stream_content.extend(b"14 TL\n")
    stream_content.extend(b"50 715 Td\n")
    
    lines = []
    for block in content.split('\n'):
        # wrap lines to ~80 chars
        words = block.split(' ')
        curr_line = ""
        for w in words:
            if len(curr_line) + len(w) + 1 > 80:
                lines.append(curr_line)
                curr_line = w
            else:
                curr_line = curr_line + " " + w if curr_line else w
        if curr_line:
            lines.append(curr_line)
        else:
            lines.append("") # empty line for paragraph
            
    y_pos = 715
    for l in lines:
        if not l.strip():
            stream_content.extend(b"0 -14 Td\n")
            y_pos -= 14
            continue
        escaped_l = l.replace('\\', '\\\\').replace('(', '\\(').replace(')', '\\)')
        stream_content.extend(f"({escaped_l}) Tj T*\n".encode('utf-8'))
        y_pos -= 14
        if y_pos < 50:
            break
            
    stream_content.extend(b"ET\n")
    
    page_dict = f"<< /Type /Page /Parent 2 0 R /Resources << /Font << /F1 3 0 R /F2 4 0 R >> >> /MediaBox [0 0 595 842] /Contents {content_id} 0 R >>"
    writer.objects.append((page_id, page_dict.encode('ascii')))
    writer.objects.append((content_id, f"<< /Length {len(stream_content)} >>\nstream\n".encode('ascii') + stream_content + b"\nendstream"))
    
    return writer.build()

def generate_report_files(report_id, title, content):
    try:
        # Ensure public/reports directory exists relative to current working directory
        reports_dir = os.path.join(os.getcwd(), "public", "reports")
        os.makedirs(reports_dir, exist_ok=True)
        
        md_filename = f"{report_id}.md"
        docx_filename = f"{report_id}.docx"
        pdf_filename = f"{report_id}.pdf"
        
        md_path = os.path.join(reports_dir, md_filename)
        docx_path = os.path.join(reports_dir, docx_filename)
        pdf_path = os.path.join(reports_dir, pdf_filename)
        
        # Write Markdown Output
        with open(md_path, "w", encoding="utf-8") as f:
            f.write(content)
            
        # Write DOCX Output (formatted text)
        with open(docx_path, "w", encoding="utf-8") as f:
            f.write(f"DOCUMENT TITLE: {title}\n")
            f.write("=" * 40 + "\n\n")
            f.write(content)
            
        # Write binary-encoded PDF Output
        pdf_bytes = generate_pdf_binary(title, content)
        with open(pdf_path, "wb") as f:
            f.write(pdf_bytes)
            
        return {
            "pdf": f"/reports/{pdf_filename}",
            "docx": f"/reports/{docx_filename}",
            "md": f"/reports/{md_filename}"
        }
    except Exception as e:
        sys.stderr.write("Error generating files\n")
        return {
            "pdf": "#",
            "docx": "#",
            "md": "#"
        }

